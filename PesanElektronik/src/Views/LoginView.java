    private boolean login(String username, char[] password) throws SQLException {
        String loginPassword = String.copyValueOf(password);
        
        UserSource loginUser = new UserSource();

        rs = loginUser.login(username, loginPassword);
        
        while(rs.next()) {
            if(rs.getRow() == 1) {
                Session.setUserLoginId(rs.getInt("id"));
                Session.setUserLoginUsername(rs.getString("username"));
                Session.setUserLoginRole(rs.getString("role"));
                
                return true;       
            } else {
                return false;
            }
        }
        
        return false;
    }

