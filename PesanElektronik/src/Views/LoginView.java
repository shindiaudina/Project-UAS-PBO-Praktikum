    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = inputUsername.getText();
        char[] password = inputPassword.getPassword();
        
        if(username.length() == 0 || password.length == 0) {
           JOptionPane.showMessageDialog(null, "Sepertinya ada field yang masih kosong.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                boolean doneLogin = login(username, password);
                
                if(doneLogin) {
                    dispose();
                
                    if("Member".equals(Session.getUserLoginRole())) {
                        UserView MainMenu = new UserView();

                        MainMenu.getContentPane().setBackground(Color.white);
                        MainMenu.setVisible(true);
                        MainMenu.setLocationRelativeTo(null);
                    } else if("Admin".equals(Session.getUserLoginRole())) {
                        AdminView MainMenu = new AdminView();

                        MainMenu.getContentPane().setBackground(Color.white);
                        MainMenu.setVisible(true);
                        MainMenu.setLocationRelativeTo(null);   
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Sepertinya Username atau Password salah. Silahkan coba lagi.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

