    private boolean register(String name, String username, char[] password) throws SQLException {
        String registerPassword = String.copyValueOf(password);
        
        UserSource registerUser = new UserSource();
        rs = registerUser.findUsername(username);
        
        while(rs.next()) {
            if(rs.getRow() == 1) return false;
        }
            
        User user = new User(name, username, registerPassword, "Member");
        registerUser.save(user);
                
        return true;
    }
    
