    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String name = inputName.getText();
        String username = inputUsername.getText();
        char[] password = inputPassword.getPassword();
        char[] confirmPassword = inputConfirmPassword.getPassword();
        
        if(name.length() == 0 || username.length() == 0 || password.length == 0 || confirmPassword.length == 0) {
            JOptionPane.showMessageDialog(null, "Sepertinya ada field yang masih kosong.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
        } else {
            if(password.length != confirmPassword.length) {
                JOptionPane.showMessageDialog(null, "Sepertinya Password tidak sama dengan Konfirmasi Password.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
                return;
            } else {
                for(int i = 0; i < password.length; i++) {
                    if(password[i] != confirmPassword[i]) {
                        JOptionPane.showMessageDialog(null, "Sepertinya Password tidak sama dengan Konfirmasi Password.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
                        return;
                    } 
                }   
            }
            
            try {
                boolean doneRegister = register(name, username, password);
                
                if(doneRegister) {
                    JOptionPane.showMessageDialog(null, "Silahkan login untuk melanjutkan akses ke Pesan Elektronik.", "Yeay! Register berhasil", JOptionPane.INFORMATION_MESSAGE);
                    
                    dispose();

                    LoginView login = new LoginView();

                    login.getContentPane().setBackground(Color.white);
                    login.setVisible(true);
                    login.setLocationRelativeTo(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Sepertinya Username telah terdaftar. Silahkan coba dengan Username lain.", "Oops! Terjadi kesalahan", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ProfileView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

